# Exercicio 06: Dobro, Triplo e Raiz Quadrada.

number = int(input("Digite um numero para descobrir o dobro, triplo e raiz quadrada: "))

dobro = number * 2
triplo = number * 3
raiz = number ** 0.5

print(f"O número escolhido foi {number}. O dobro do valor é {dobro}, o triplo é {triplo} e a Raiz quadrada é {raiz:.1f} ")